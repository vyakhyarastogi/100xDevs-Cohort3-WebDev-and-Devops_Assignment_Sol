// Assignment #1 - Write a function that calculates the sum of two arguments


// Function to calculate the sum of two arguments and return the result
function sum(a: number, b: number): number {
    // Return the sum of the two arguments
    return a + b;
}

// Call the function and store the result in a variable called ans
const ans = sum(5, 10);

// Print the result to the console
console.log(ans); // 15